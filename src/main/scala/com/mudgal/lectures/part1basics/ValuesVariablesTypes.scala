package com.mudgal.lectures.part1basics

object ValuesVariablesTypes extends App {
  val x: Int = 42
  println(x)

  //  VALS are immutable
  //  Compoler can infer types


  val aString: String = "hello"
  println(aString)

  val aBoolean: Boolean = false
  val aChar: Char = 'a'
  val aInt: Int = x
  val aShort: Short = 123
  val aLong: Long = 1233456676134213L
  val aFloat: Float = 12.23f
  val aDouble: Double = 12.21


  //  var variables
  var aVariable : Int = 4
  aVariable = 5 // side-effects




}
